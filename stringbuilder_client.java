public class stringbuilder_client {
    public static void main(String[] args) {
        StringBuilder();
    }

    public static void StringBuilder() {
        String sb = new String();
        for (int i = 0; i < 1000; i++) {
            sb.append(i);
        }
    }

    public static void stringbuillder() {
        String s = "";

        for (int i = 0; i < 10000; i++) {
            s += i;
        }
    }
}
